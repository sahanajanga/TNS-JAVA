package com.tnsif.methods;

public class MethodOverloading {
	public void show()
	{
		System.out.println("No Parameter");
	}
	public void show( int a,float b)
	{
		System.out.println("Integer and Float Parameters");
	}
	public void show(double a,float b,int c)
	{
	System.out.println("Double and Flaot and Integer parameters");
	}

}
