package com.tnsif.demo;

public class Relational {

	public static void main(String[] args) {
		int a=6;
		int b=4;
		System.out.println(a==b);
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a!=b);
	}

}
